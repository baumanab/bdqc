	static int h[256];
	memset( h, 0, sizeof(h) );
	for(;;) {
		const int C = getc( stdin );
		if( C < 0 )
			break;
		h[C] += 1;	
	}
	for(int i =   0; i < 128; i++ )
		printf( "%s\t%d\n", ASCII_CHAR_NAMES[i], h[i] );
	for(int i = 128; i < 256; i++ )
		printf( "%d\t%d\n", i, h[i] );

	return EXIT_SUCCESS;
